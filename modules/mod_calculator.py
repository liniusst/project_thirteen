def value_plus(number_one: int, number_two: int) -> int:
    execute = number_one + number_two
    return execute

def value_minus(number_one: int, number_two: int) -> int:
    execute = number_one - number_two
    return execute

def value_prod(number_one: int, number_two:int) -> int:
    execute = number_one * number_two
    return execute

def value_div(number_one: int, number_two:int) -> int:
    execute = number_one // number_two
    return execute